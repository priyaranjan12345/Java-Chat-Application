    private void sendAllMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendAllMouseClicked
        String myname = userTitle.getText().trim();
        int[] selectedId = ListUser.getSelectedIndices();
        ArrayList<String> users = new ArrayList<>();
        users.add(myname);
        for (int i = 0; i < selectedId.length; i++) {
            users.add(ListUser.getModel().getElementAt(selectedId[i]));
        }

        String selected = "Selected";
        if (isAllSelected) {
            selected = "All";
        }

        if (users.size() <= 1) {
            System.out.println("not selected user");
        } else {
            String msg = textMsg.getText();
            cs.sendMessage(msg, users, userTitle.getText(), selected);
        }
        textMsg.setText("");
    }//GEN-LAST:event_sendAllMouseClicked

