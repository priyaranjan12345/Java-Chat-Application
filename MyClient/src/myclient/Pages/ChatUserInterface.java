    private void userTitle2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_userTitle2MouseClicked
        if (isAllSelected) {
            isAllSelected = false;
            ListUser.clearSelection();
        } else {
            isAllSelected = true;
            int begn = 0;
            int end = ListUser.getModel().getSize() - 1;
            if (end >= 0) {
                ListUser.setSelectionInterval(begn, end);
            }
        }
    }//GEN-LAST:event_userTitle2MouseClicked

