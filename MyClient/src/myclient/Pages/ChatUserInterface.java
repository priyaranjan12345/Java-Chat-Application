    public void sendEmoji(String name) {
        String myname = userTitle.getText().trim();
        int[] selectedId = ListUser.getSelectedIndices();
        ArrayList<String> users = new ArrayList<>();
        users.add(myname);
        for (int i = 0; i < selectedId.length; i++) {
            users.add(ListUser.getModel().getElementAt(selectedId[i]));
        }

        int jlistsize = ListUser.getModel().getSize();
        int selectedUserSize = users.size() - 1;
        String selected = "Selected";
        if (jlistsize == selectedUserSize) {
            selected = "All";
        }

        if (users.size() <= 1) {
            System.out.println("not selected user");
        } else {

            try {
                File file = new File("src/");
                String absolutepath = file.getAbsolutePath().replace("\\", "//");
                file = new File(absolutepath + "//emoji//"+name);

                cs.sendEmoji(selected, users, myname, file);

            } catch (Exception e) {
                System.out.println("Error emoji :" + e.getMessage());
            }
        }
    }

